#!/usr/bin/env node
/**
 * Restore Fragmented Observations
 *
 * In v2.0.0, a bug iterated observation arrays character-by-character,
 * creating single-letter observations instead of full sentences.
 *
 * This script:
 * 1. Finds runs of single-character observations on the same entity
 * 2. Groups them by entity_id and sequential ID runs
 * 3. Concatenates them back into the original text
 * 4. Prints the restorations for review
 * 5. Outputs SQL to apply the fix
 *
 * Usage:
 *   # Preview what will be restored:
 *   npx wrangler d1 execute YOUR_DB --remote --command "SELECT id, entity_id, content, added_at FROM observations WHERE LENGTH(TRIM(content)) <= 1 ORDER BY entity_id, id" --json > fragments.json
 *   node scripts/restore-fragmented.js fragments.json
 *
 *   # Apply the generated SQL:
 *   npx wrangler d1 execute YOUR_DB --remote --file=restore-output.sql
 */

const fs = require('fs');

const inputFile = process.argv[2];
if (!inputFile) {
  console.log(`
Restore Fragmented Observations
================================

Step 1: Export fragments from your database:

  npx wrangler d1 execute YOUR_DB_NAME --remote \\
    --command "SELECT id, entity_id, content, added_at FROM observations WHERE LENGTH(TRIM(content)) <= 1 ORDER BY entity_id, id" \\
    --json > fragments.json

Step 2: Run this script to generate restoration SQL:

  node scripts/restore-fragmented.js fragments.json

Step 3: Review the output, then apply:

  npx wrangler d1 execute YOUR_DB_NAME --remote --file=restore-output.sql
`);
  process.exit(0);
}

// Parse the wrangler D1 JSON output
let raw;
try {
  raw = JSON.parse(fs.readFileSync(inputFile, 'utf-8'));
} catch (e) {
  console.error(`Error reading ${inputFile}: ${e.message}`);
  process.exit(1);
}

// Wrangler D1 JSON format: array of result objects with .results
const rows = Array.isArray(raw) ? (raw[0]?.results || raw) : (raw.results || [raw]);

if (!rows.length) {
  console.log('No fragmented observations found. Database is clean.');
  process.exit(0);
}

console.log(`Found ${rows.length} single-character observations.\n`);

// Group into runs: sequential IDs on the same entity_id
const runs = [];
let currentRun = null;

for (const row of rows) {
  if (currentRun &&
      row.entity_id === currentRun.entity_id &&
      row.id === currentRun.lastId + 1) {
    // Continue current run
    currentRun.chars.push(row.content);
    currentRun.ids.push(row.id);
    currentRun.lastId = row.id;
  } else {
    // Start new run
    if (currentRun && currentRun.chars.length > 1) {
      runs.push(currentRun);
    }
    currentRun = {
      entity_id: row.entity_id,
      firstId: row.id,
      lastId: row.id,
      ids: [row.id],
      chars: [row.content],
      added_at: row.added_at
    };
  }
}
// Don't forget the last run
if (currentRun && currentRun.chars.length > 1) {
  runs.push(currentRun);
}

if (!runs.length) {
  console.log('No sequential character runs found. Fragments may be isolated single characters.');
  console.log('You can safely delete isolated single-character observations with:');
  console.log('  DELETE FROM observations WHERE LENGTH(TRIM(content)) <= 1;');
  process.exit(0);
}

console.log(`Detected ${runs.length} fragmented observation(s) to restore:\n`);
console.log('─'.repeat(70));

const sqlLines = [];

for (let i = 0; i < runs.length; i++) {
  const run = runs[i];
  const restored = run.chars.join('');
  const escapedContent = restored.replace(/'/g, "''");

  console.log(`\n[${i + 1}] Entity ID: ${run.entity_id}`);
  console.log(`    Fragment IDs: ${run.firstId}–${run.lastId} (${run.ids.length} characters)`);
  console.log(`    Restored text: "${restored}"`);
  console.log(`    Date: ${run.added_at}`);

  // Keep the first ID, update its content to the restored text
  sqlLines.push(`-- Restore: "${restored.slice(0, 60)}${restored.length > 60 ? '...' : ''}"`);
  sqlLines.push(`UPDATE observations SET content = '${escapedContent}' WHERE id = ${run.firstId};`);

  // Delete all the other fragment IDs
  const deleteIds = run.ids.slice(1);
  if (deleteIds.length > 0) {
    sqlLines.push(`DELETE FROM observations WHERE id IN (${deleteIds.join(',')});`);
  }
  sqlLines.push('');
}

console.log('\n' + '─'.repeat(70));
console.log(`\nTotal: ${runs.length} observations restored from ${rows.length} fragments.`);

// Write SQL output
const outputFile = 'restore-output.sql';
const sqlContent = [
  '-- Auto-generated by restore-fragmented.js',
  `-- Restores ${runs.length} fragmented observations`,
  `-- Generated: ${new Date().toISOString()}`,
  '',
  ...sqlLines
].join('\n');

fs.writeFileSync(outputFile, sqlContent);
console.log(`\nSQL written to: ${outputFile}`);
console.log(`Review it, then run:`);
console.log(`  npx wrangler d1 execute YOUR_DB_NAME --remote --file=${outputFile}`);
